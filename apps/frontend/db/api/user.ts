import { prisma } from "@/db/prisma";

export const getUserByEmail = async (email: string) => {
  try {
    const user = await prisma.user.findUnique({
      where: { email },
    });
    return user;
  } catch (error) {
    console.error("Error occurred at getUserByEmail ", error);
    throw error;
  }
};

